#ifndef INC_STDINT_H_
#define INC_STDINT_H_


/* 8-bit, 16-bit , 32-bit signed type. */
typedef char  int8_t;
typedef short int16_t;
typedef long  int32_t;

/* 8-bit, 16-bit , 32-bit unsigned type. */
typedef unsigned char uint8_t;
typedef unsigned int  uint16_t;
typedef unsigned long uint32_t;



#endif /* INC_STDINT_H_ */
